# Variáveis são a  forma de armazenar dados na memoria do computador. 
# Podemos declarar variáveis, atribuir valores a elas e imprimir seus valores. 
# Elas podem receber diversos  tipos de valores, como: strings, numeros, boleanos, listas, tuplas e dicionários. 

# Exemplos de variáveis de declarar uma variável e atribuir um valor  a ela: 

fruta = 'Pera'

#  Exemplo de printar um valor de varável: 
print(fruta)


# Tipos de  dados que uma variável pode receber: 
# strings: 
nome = 'Guilherme' 
print(nome)

#números inteiros e reais:
idade = 25
print(idade)
altura = 1.80
print(altura)

#booleanos: 
isTrue = True
print(isTrue)

#listas: 
alunos = ['Guilherme', 'Sergio', 'Thiago', 'Joaquim']
print(alunos) 

#tuplas: 
pessoas = ('Guilherme', 'Sergio', 'Thiago', 'Joaquim')
print(pessoas)

#dicionários: 
pessoa = {'nome': 'Guilherme', 'idade': 25, 'altura': 1.80}
print(pessoa)




